public class DatuPertsonal {
    private String txoriIzena;
    private String jabeIzena;

    public DatuPertsonal(String txoriIzena, String jabeIzena) {
        this.txoriIzena = txoriIzena;
        this.jabeIzena = jabeIzena;
    }

    public String getTxoriIzena() {
        return txoriIzena;
    }

    public String getJabeIzena() {
        return jabeIzena;
    }

    public void setTxoriIzena(String txoriIzena) {
        this.txoriIzena = txoriIzena;
    }

    public void setJabeIzena(String jabeIzena) {
        this.jabeIzena = jabeIzena;
    }
}
